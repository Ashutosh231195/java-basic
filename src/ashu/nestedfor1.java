package ashu;

public class nestedfor1 {
	public static void main (String[] arg) {

   for  (int j=5;j>=1;j--)
   {
	for(int i=1;i<=j;i++) {
		System.out.print("*");}
	
for(int k=4;k<=j;k++) {
		System.out.print("");}
	
	for(int k=3;k<=j;k++) {
		System.out.print("  ");}
	for(int k=5;k<=j;k++) {
		System.out.print(" ");
	}

for(int i=1;i<=j;i++) {
	System.out.print("*");}
System.out.println();
}
	}
}
	

  